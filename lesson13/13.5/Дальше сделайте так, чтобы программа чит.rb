Для любознательных:

На уроке мы писали текст класса в том же файле, что и код, которые его использует. В руби принято выносить код каждого класса в отдельный файл.

Напишите код класса Question в файле с названием question.rb, положите файл в папку lib, а в основной программе подключите этот класс так:

require_relative 'lib/question'
Подробнее про это будем говорить в следующих уроках.

