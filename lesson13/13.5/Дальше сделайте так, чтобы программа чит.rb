Дальше сделайте так, чтобы программа читала все вопросы из папки data, 

выбирала из них случайным образом 5 и задавала бы их пользователю. 

Задав вопрос, программа ждет ответа пользователя в консоли и говорит, правильно ли он ответил.

Если ответ верный — программа добавляет баллы за вопрос к счету пользователя, а если неверный — сообщает правильный ответ.

В конце программа должна написать, сколько правильных ответов дал пользователь и сколько баллов он набрал.

Пример запуска:

Мини-викторина. Ответьте на вопросы.

Сколько байт в килобайте? (5 баллов)
> 1024
Верный ответ!

В каком году Гагарин полетел в космос? (10 баллов)
> 1962
Неправильно. Правильный ответ: 1961

Сколько дней в високосном году? (3 балла)
> 366
Верный ответ!

Какая дата Куликовской битвы? (10 баллов)
> 1400
Неправильно. Правильный ответ: 1380

Сколько цветов в радуге? (1 балла)
